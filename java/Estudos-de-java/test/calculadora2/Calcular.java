package test.calculadora2;

public class Calcular {
    public void soma(float n1, float n2 ){
        System.out.println("A soma de "+n1+" mais "+n2+" = "+(n1+n2));
    }
    public void subitrai(float n1, float n2){
        System.out.println("Subitraido "+n2+" de "+n1+" = "+(n1-n2));
    }
    public void divide(float n1, float n2){
        System.out.println("Dividido "+n1+" por "+n2+" + "+(n1/n2));
    }
    public void mutiplica(float n1, float n2){
        System.out.println("A mutiplacação de "+n1+" por "+n2+" = "+(n1*n2));
    }
}


